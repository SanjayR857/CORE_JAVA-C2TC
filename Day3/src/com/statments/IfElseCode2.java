//if else statement 
public class IfElseCode2 {
public static void main(String[] args) {
	int x=2;
	int y=3;
	if(x+y<2) {
		System.out.println("x+y is less than 10");
	}
	else {
		System.out.println("x+y is greater than 20");
	}
}
}
